
// Tipos de datos en JavaScript

// ==================== NÃºmeros ====================

let number1 = 5;
console.log(number1);
console.log(typeof number1);

let number2 = 3.8;
console.log(number2);
console.log(typeof number1);

// ==================== Strings ====================

let string = "Hola mundo";
console.log(string);
console.log(typeof string);

let nombre = "Jimena";
console.log(nombre);
console.log(typeof nombre);

let letra = 'a';
console.log(letra);
console.log(typeof letra);

// ==================== Boolean ====================

let boolean = true;
console.log(boolean);
console.log(typeof boolean);

let boolean2 = false;
console.log(boolean2);
console.log(typeof boolean2);

// ==================== Undefined ====================

let undefinedVar;
console.log(undefinedVar);
console.log(typeof undefinedVar);

// ==================== Null ====================

let nullVar = null;
console.log(nullVar);
console.log(typeof nullVar);

// ==================== Object ====================

class Persona {
    constructor(nombre, apellido) {
        this.nombre = nombre;
        this.apellido = apellido;
    }
}

console.log(Persona);
console.log(typeof Persona);

// ==================== Function ====================

function saludar() {
    console.log("Hola");
}

console.log(saludar);
console.log(typeof saludar);

// ==================== Array ====================

let array = [1, 2, 3, 4, 5];
console.log(array);
console.log(typeof array);








